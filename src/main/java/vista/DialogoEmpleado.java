    private void comboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboTipoActionPerformed
        if(this.comboTipo.getSelectedItem()=="FIJO"){
            this.labelHoras.setVisible(false);
            this.spinnerHoras.setVisible(false);
        }
        if(this.comboTipo.getSelectedItem()=="EVENTUAL"){
            this.labelHoras.setVisible(true);
            this.spinnerHoras.setVisible(true);
        }
    }//GEN-LAST:event_comboTipoActionPerformed

