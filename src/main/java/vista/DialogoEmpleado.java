    public String getDni(){
        return textDni.getText();
    }
    public String getNombre(){
        return textNombre.getText();
    }
    public String getTipo(){
        return (String)comboTipo.getSelectedItem();
    }
    public float getSalario(){
        if(!textSalario.getText().isEmpty()){
            return Float.parseFloat(textSalario.getText());
        }
        else{
            throw new NumberFormatException("Salario incorrecto");
        }
    }
    public int getHoras(){
        return (Integer)spinnerHoras.getValue();
    }
    public int mostrar(){
        opcion=CANCELAR;
        textDni.setText("");
        textNombre.setText("");
        textSalario.setText("");
        spinnerHoras.setValue(0);
        setVisible(true);
        return opcion;
    }
    
